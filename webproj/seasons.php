<h1>Agricultural Seasons in India</h1>
<p class="fst">1. The Kharif Season:
<ul>
	<li class="thrd">Crops are sown at the be­ginning of south-west monsoon and harvested at the end of the south-west monsoon.</li>
	<li class="thrd">Sowing Season: May to July.</li>
	<li class="thrd">Harvesting Season : September to October.</li>
	<li class="thrd">Important Crops: Jowar, Bajra, Rice, Maize, Cotton, Groundnut, Jute, Hemp, Tobacco etc.</li></p></ul>
<p class="fst">2. The Rabi Season:
<ul>
	<li class="thrd">Crops need cool climate during growth period but warm climate during the germination of seed and maturation.</li>
<li class="thrd">Sowing Season: October to December </li>
<li class="thrd">Harvesting Season: February to April </li>
<li class="thrd">Important Crops: Wheat, Barley, Gram, Linseed, Mustard, Masoor & Peas.</li></ul></p>
<p class="fst">3. The Zaid Season:
	<ul>
		<li class="thrd">These Crops are raised throughout the year due to artificial irrigation.</li>
		<ul>

<p class="sec">1. Zaid Kharif Crops:

<li class="thrd">Sowing Season: August to September</li>

<li class="thrd">Harvesting Season: December-January</li>

<li class="thrd">Important Crops: Rice, Jowar, Rapeseed, Cotton, Oilseeds.</li></ul>
<ul>
<p class="sec">2. Zaid Rabi Crops:</p>


<li class="thrd">Sowing Season: February to March.</li>

<li class="thrd">Harvesting Season: April-May.</li>

<li class="thrd">Important Crops: Watermelon, Toris, Cucum­ber & other vegetables.</li></ul>
